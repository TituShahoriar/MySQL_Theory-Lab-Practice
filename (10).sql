10.How do you use aggregate functions like COUNT, SUM, AVG, MIN, and MAX in MySQL queries?
Here are some examples:

sql

SELECT COUNT(*) FROM table_name; -- Returns the number of rows in the table

SELECT SUM(column_name) FROM table_name; -- Returns the sum of values in the column

SELECT AVG(column_name) FROM table_name; -- Returns the average of values in the column

SELECT MIN(column_name) FROM table_name; -- Returns the minimum value in the column

SELECT MAX(column_name) FROM table_name; -- Returns the max